// Arithematic operators
let a = 10, b = 20;
console.log(a + b);
console.log(a - b);
console.log(a * b)
console.log(a / b)
console.log(a % b)
console.log(2 ** 3);

// relational operators
console.log(5 > 3);
console.log(5 < 3)
console.log(5 >= 3)
console.log(5 <= 3)
a = 1, b = true;
console.log(a == b);
console.log(a === b);
console.log(a != b);
console.log(a !== b);

// logical operators
a = 0, b = 20;
console.log((a > 5) && (b > 5))
console.log(a > 5 || b > 5)
console.log(!(a > 10));

// increment and decrement
a = 10;
// ++a pre increment -- update & value
// a++ post increment -- value & update
let x = ++a + a++;
console.log(x)

a = 10;
x = a++ + --a + ++a + a--;

a = 5, b = 3
x = a++ + ++b + ++a + b++;


// ternary operator or conditional operator
a = 5, b = 3;
x = (a > b) ? 25 : 30;









































